class Relasional {

	public static void main(String[] args){
		// definisi variabel
		int x = 10;
		int y = 20;

		// menggunakan operator relasional
		System.out.println(x != y);

		if(x != y){
			System.out.println("Benar");
		}
		else{
			System.out.println("Salah");
		}
	}
}

